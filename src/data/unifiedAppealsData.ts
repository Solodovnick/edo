// UNIFIED APPEALS DATA - единый источник данных для всех кабинетов
// Используется в: Ответственный, Руководитель, Аудитор

export interface UnifiedAppeal {
  // БАЗОВЫЕ ПОЛЯ
  id: string;
  regDate: string;
  category: 'Письменное' | 'Устное' | 'Регулятор';
  status: string;
  deadline: string;
  responsible: string;
  applicantName: string;
  organizationName: string;
  address: string;
  phone: string;
  type: 'Физ лицо' | 'Юр лицо' | 'Физлицо' | 'Юрлицо';
  
  // КАТЕГОРИИ ОБРАЩЕНИЯ
  appealCategory?: string; // Категория (например: "Автодозвон", "Дистанционные сервисы")
  appealSubcategory?: string; // Подкатегория (например: "Банкоматы", "Реклама")
  
  // КОНТЕНТ
  content: string;
  solution: string;
  response: string;
  
  // СПЕЦИФИЧНЫЕ ПОЛЯ
  cbs?: string;
  isMine?: boolean; // Вычисляется динамически
  
  // АУДИТ
  auditStatus: 'pending' | 'approved' | 'rejected';
  deadlineCountdown: {
    days: number;
    hours: number;
    minutes: number;
  };
  
  // РУКОВОДИТЕЛЬ
  priority?: 'Высокий' | 'Средний' | 'Низкий';
  requiresAttention?: boolean;
  requiresSignature?: boolean;
  viewHistory?: Array<{
    userId: string;
    userName: string;
    timestamp: string;
    action: 'просмотр' | 'одобрено' | 'отклонено';
  }>;
  
  // ДОПОЛНИТЕЛЬНЫЕ ДАННЫЕ
  attachments: Array<{
    id: number;
    name: string;
    date: string;
  }>;
  history: Array<{
    number: string;
    date: string;
    status: string;
  }>;
  crmComments: Array<{
    author: string;
    date: string;
    text: string;
  }>;
  
  // МЕТАДАННЫЕ
  createdAt?: string;
  updatedAt?: string;
}

