// Функция поиска
export function searchKnowledge(query: string): KnowledgeItem[] {
  const lowerQuery = query.toLowerCase().trim();
  
  if (!lowerQuery) return knowledgeItems;

  return knowledgeItems
    .map(item => {
      let relevance = 0;

      // Поиск в том, что говорит клиент (наивысший приоритет)
      if (item.clientSays.toLowerCase().includes(lowerQuery)) {
        relevance += 10;
      }

      // Поиск в ключевых словах
      for (const keyword of item.keywords) {
        if (keyword.toLowerCase().includes(lowerQuery) || lowerQuery.includes(keyword.toLowerCase())) {
          relevance += 5;
        }
      }

      // Поиск в категории
      if (item.category.toLowerCase().includes(lowerQuery)) {
        relevance += 2;
      }

      // Поиск в подкатегории
      if (item.subcategory.toLowerCase().includes(lowerQuery)) {
        relevance += 2;
      }

      return { item, relevance };
    })
    .filter(({ relevance }) => relevance > 0)
    .sort((a, b) => b.relevance - a.relevance)
    .map(({ item }) => item);
}

// Группировка по первой букве для алфавитного указателя
export function groupByFirstLetter(): { [key: string]: KnowledgeItem[] } {
  const groups: { [key: string]: KnowledgeItem[] } = {};
  
  for (const item of knowledgeItems) {
    const firstLetter = item.clientSays[0].toUpperCase();
    if (!groups[firstLetter]) {
      groups[firstLetter] = [];
    }
    groups[firstLetter].push(item);
  }
  
  return groups;
}
