-- ==============================================================================
-- 1. ОЧИСТКА (ДЛЯ ИДЕМПОТЕНТНОСТИ)
-- ==============================================================================
DROP SCHEMA public CASCADE;
CREATE SCHEMA public;
GRANT ALL ON SCHEMA public TO postgres;
GRANT ALL ON SCHEMA public TO public;

-- ==============================================================================
-- 2. СОЗДАНИЕ ПОЛЬЗОВАТЕЛЬСКИХ ТИПОВ (ENUM)
-- ==============================================================================
CREATE TYPE public.appeal_priority_enum AS ENUM ('низкое', 'средний', 'высокий');
CREATE TYPE public.appeal_type_enum AS ENUM ('устное', 'письменное', 'регулятор');
CREATE TYPE public.applicant_type_enum AS ENUM ('физлицо', 'юрлицо');
CREATE TYPE public.incident_status_enum AS ENUM ('решено');

-- ==============================================================================
-- 3. СОЗДАНИЕ ТАБЛИЦ (ИЗ ДАМПА)
-- ==============================================================================

CREATE TABLE public.roles (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    role_name character varying(255) NOT NULL,
    role_description character varying(255) NOT NULL,
    is_active boolean NOT NULL,
    created_at timestamp(0) with time zone NOT NULL,
    created_by bigint NOT NULL
);

CREATE TABLE public.statuses (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    status_name character varying(255) NOT NULL,
    status_description text NOT NULL,
    is_active boolean NOT NULL,
    created_at timestamp(0) with time zone NOT NULL,
    created_by timestamp(0) with time zone NOT NULL
);

CREATE TABLE public.users (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    first_name character varying(255) NOT NULL,
    last_name character varying(255) NOT NULL,
    patronymic character varying(255) NOT NULL,
    "position" character varying(255) NOT NULL,
    role_id bigint NOT NULL REFERENCES public.roles(id),
    new_column bigint NOT NULL,
    is_active boolean NOT NULL,
    created_at timestamp(0) with time zone NOT NULL,
    created_by bigint NOT NULL
);

CREATE TABLE public.users_roles (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id bigint NOT NULL REFERENCES public.users(id),
    role_id bigint NOT NULL REFERENCES public.roles(id)
);

CREATE TABLE public.audit_templates (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    template_name character varying(255) NOT NULL,
    created_at timestamp(0) with time zone NOT NULL,
    created_by bigint NOT NULL
);

CREATE TABLE public.audit (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    started_at timestamp(0) with time zone NOT NULL,
    user_id bigint NOT NULL REFERENCES public.users(id),
    audit_template_id bigint NOT NULL REFERENCES public.audit_templates(id)
);

CREATE TABLE public.audit_questions (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    template_id bigint NOT NULL REFERENCES public.audit_templates(id),
    question_description bigint NOT NULL,
    created_at timestamp(0) with time zone NOT NULL,
    created_by bigint NOT NULL
);

CREATE TABLE public.feedback (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    audit_id bigint NOT NULL REFERENCES public.audit(id),
    assessment bigint NOT NULL,
    question_id bigint NOT NULL,
    answer text NOT NULL
);

CREATE TABLE public.appeals (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    letter_number integer NOT NULL,
    letter_received_at timestamp(0) with time zone NOT NULL,
    applicant_type public.applicant_type_enum NOT NULL,
    appeal_type public.appeal_type_enum NOT NULL,
    appeal_category_id bigint NOT NULL,
    appeal_subcategory_id bigint NOT NULL,
    appeal_priority public.appeal_priority_enum NOT NULL,
    applicant_id bigint NOT NULL,
    appeal_description text NOT NULL,
    appeal_status bigint NOT NULL REFERENCES public.statuses(id),
    user_id bigint NOT NULL REFERENCES public.users(id),
    answer_id bigint NOT NULL,
    commission_decision_id bigint NOT NULL,
    created_at timestamp(0) with time zone NOT NULL,
    created_by bigint NOT NULL,
    planned_processing_at timestamp(0) with time zone NOT NULL,
    processed_at timestamp(0) with time zone NOT NULL,
    audit_id bigint NOT NULL REFERENCES public.audit(id)
);

CREATE TABLE public.attachments (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    appeal_id bigint NOT NULL REFERENCES public.appeals(id),
    s3_key character varying(255) NOT NULL,
    original_filename character varying(255) NOT NULL,
    content_type character varying(255) NOT NULL,
    file_size bigint NOT NULL,
    uploaded_at timestamp(0) with time zone NOT NULL,
    uploaded_by bigint NOT NULL
);

CREATE TABLE public.incidents (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    appeal_id bigint NOT NULL REFERENCES public.appeals(id),
    created_at timestamp(0) with time zone NOT NULL,
    target_resolution_date timestamp(0) with time zone NOT NULL,
    applicant_full_name character varying(255) NOT NULL,
    applicant_phone character varying(255) NOT NULL,
    appeal_description text NOT NULL,
    user_comments text NOT NULL,
    attachment_id bigint NOT NULL,
    incident_processed_at timestamp(0) with time zone NOT NULL,
    hd_user_id bigint NOT NULL,
    incident_status public.incident_status_enum NOT NULL
);

CREATE TABLE public.requests_to_business_units (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at timestamp(0) with time zone NOT NULL,
    created_by bigint NOT NULL,
    request_planned_processing_date timestamp(0) with time zone NOT NULL,
    request_processed_at timestamp(0) with time zone NOT NULL,
    request_answer text NOT NULL,
    bu_comment text NOT NULL,
    appeal_id bigint NOT NULL REFERENCES public.appeals(id)
);

-- ==============================================================================
-- 4. СОЗДАНИЕ ТАБЛИЦ ИЗ СЛОВАРЯ ДАННЫХ (которых не было в дампе)
-- ==============================================================================

CREATE TABLE public.response_templates (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name_response character varying(50) NOT NULL,
    description_name_response character varying(500) NOT NULL,
    template_structure character varying(2000) NOT NULL,
    active boolean DEFAULT false NOT NULL,
    created_at timestamp(0) with time zone NOT NULL,
    created_by bigint NOT NULL,
    updated_at timestamp(0) with time zone,
    edited_by bigint
);

CREATE TABLE public.calls (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    appeal_id bigint NOT NULL REFERENCES public.appeals(id),
    call_datetime timestamp(0) with time zone NOT NULL
);

CREATE TABLE public.rejection_statements (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_by bigint NOT NULL REFERENCES public.users(id),
    manager_id bigint NOT NULL REFERENCES public.users(id),
    rejection_reason bigint NOT NULL,
    executor_comment text NOT NULL,
    created_at timestamp(0) with time zone NOT NULL,
    reviewed_at timestamp(0) with time zone,
    review_result boolean,
    manager_comment text
);

